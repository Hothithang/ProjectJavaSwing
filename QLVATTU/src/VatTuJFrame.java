    public void ganDuLieu() {
        PreparedStatement pst = null;
        Connection conn = null;
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost/qlvattu","root","Thang123!");
            String sql = "SELECT * FROM VATTU";
            pst = conn.prepareStatement(sql);
            ResultSet resultSet = pst.executeQuery();
            jTableVatTu.setModel(DbUtils.resultSetToTableModel(resultSet));
            
            if(jTableVatTu.getRowCount() > 0) {
                DefaultTableModel tblModel = (DefaultTableModel)jTableVatTu.getModel();
                
                String mavt = tblModel.getValueAt(0,0).toString();
                String tenvt = tblModel.getValueAt(0,1).toString();
                String dvt = tblModel.getValueAt(0,2).toString();
                String giamua = tblModel.getValueAt(0,3).toString();
                String slton = tblModel.getValueAt(0,4).toString();
                
                jTextFieldMa.setText(mavt);
                jTextFieldMa.setEditable(false);
                jTextFieldTen.setText(tenvt);
                jTextFieldTen.setEditable(false);
                jTextFieldDVT.setText(dvt);
                jTextFieldDVT.setEditable(false);
                jTextFieldGiaMua.setText(giamua);
                jTextFieldGiaMua.setEditable(false);
                jTextFieldSoLuongTon.setText(slton);
                jTextFieldSoLuongTon.setEditable(false);
            } else {
                jButtonSua.setEnabled(false);
                jButtonXoa.setEnabled(false);
                jTextFieldMa.setText("");
                jTextFieldTen.setText("");
                jTextFieldDVT.setText("");
                jTextFieldGiaMua.setText("");
                jTextFieldSoLuongTon.setText("");
                
            }
            jButtonLuu.setEnabled(false);
            jButtonKhongLuu.setEnabled(false);
            jButtonXoa.setEnabled(false);
                
        } catch (SQLException ex) {
            String msg = "MyModel(): Error Connecting to Database:\n" + ex.getMessage();
            System.out.println(msg);
        } 
    }


 
