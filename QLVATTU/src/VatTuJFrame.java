    private void jButtonTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTimKiemActionPerformed
        // TODO add your handling code here:
        String mavt = jTextFieldTKNoiDung.getText();
        PreparedStatement pst = null;
        Connection conn = null;
        
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost/qlvattu","root","Thang123!");
            String sql = "SELECT * FROM VATTU WHERE MAVT LIKE '%"+mavt+"%'";
            pst = conn.prepareStatement(sql);
            ResultSet resultSet = pst.executeQuery();
            jTableVatTu.setModel(DbUtils.resultSetToTableModel(resultSet));
            
            if(jTableVatTu.getRowCount() > 0) {
                jTextFieldMa.setText("");
                jTextFieldTen.setText("");
                jTextFieldDVT.setText("");
                jTextFieldGiaMua.setText("");
                jTextFieldSoLuongTon.setText("");
            } else {
                if(!resultSet.next()) {
                    JOptionPane.showMessageDialog(null, "Mã vật tư không tìm thấy");
                    jTextFieldTKNoiDung.setText("");
                    ganDuLieu();
                }
                
            }
            
                
        } catch (SQLException ex) {
            String msg = "MyModel(): Error Connecting to Database:\n" + ex.getMessage();
            System.out.println(msg);
        } 
    }//GEN-LAST:event_jButtonTimKiemActionPerformed

